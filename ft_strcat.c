#include "libft.h"

char	*ft_strcat(char *s, const char *c)
{
	int		i;
	int		j;

	j = 0;
	i = ft_strlen(s);
	while (c[j])
		s[i++] = c[j++];
	s[i] = '\0';
	return ((char *)s);
}
